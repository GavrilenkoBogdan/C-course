//Лабораторна робота 1. Розробка алгоритмів з лінійною структурою
#include <iostream>
#define _USE_MATH_DEFINES
#include <math.h>

using namespace std;

int main()
{
	//Приклад 4
	int a4 = 2;
	float b4 = 11.05;
	float y4;

	y4 = pow((0, 1 * log(exp(3)) / pow(sin((a4 / b4) * M_PI), 2) + pow(cos((a4 / b4) * M_PI), 2)), 1. / 3);
	cout << "Приклад номер 4: " << y4 << "\n";

	//Приклад 9
	int a9 = 3;
	float b9 = 0.521;
	float y9;

	y9 = pow((pow((0, 127 * exp(b9 / a9) / 1 - pow((cos((b9 / a9) * M_PI)), 1. / 3)), 2)), 1. / 3);
	cout << "Приклад номер 9: " << y9 << "\n";

	return 0;
}